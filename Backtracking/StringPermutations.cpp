void permute(string s, int i) {
    if (i == s.length()) {
        cout << s << endl;
    }
    for (int j = i; j < s.length(); j++) {
        swap(s[i], s[j]);
        permute(s, i + 1);
        swap(s[i], s[j]);  // backtracking to restore this level string
    }
}

void solve() {
    string s;
    cin >> s;
    permute(s, 0);
}

