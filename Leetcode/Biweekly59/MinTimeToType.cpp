void solve() {
    string word;
    cin >> word;
    int time = 0;
    int present = 0;
    for (char c : word) {
        int minValue =
            min((abs((c - 'a') - (present + 26))), (abs((c - 'a') - present)));
        time += min(minValue, (abs(((c - 'a') + 26) - present)));
        present = c - 'a';
        time++;
        // cout << time << " ";
    }
    cout << time << endl;
}

