void solve() {
    int n;
    cin >> n;
    int negNumbers = (n % 2 == 0 ? n : n - 1);
    unordered_map<int, int> rows;
    unordered_map<int, int> columns;
    for (int i = 0; i < n; i++) {
        rows[i] = 0;
        columns[i] = 0;
    }
    vector<vector<int>> matrix(n, vector<int>(n, 0));
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            if (rows[i] < negNumbers && columns[j] < negNumbers) {
                matrix[i][j] = -1;
                rows[i]++;
                columns[j]++;
            } else
                matrix[i][j] = 1;
        }
    }

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cout << matrix[i][j] << " ";
        }
        cout << endl;
    }
}

int32_t main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}