void solve() {
    int n;
    cin >> n;
    vi arr(n);
    inputarr(arr, n);
    int summ = accumulate(arr.begin(), arr.end(), 0);
    unordered_set<int> uniqueVals;
    int i = 1;
    int ans = 0;
    while (i < summ) {
        summ = summ - i;
        // cout << i << " " << summ << endl;
        ans++;
        i++;
    }
    cout << ans << endl;
}

